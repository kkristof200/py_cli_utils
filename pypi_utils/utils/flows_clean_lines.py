# --------------------------------------------------------------- Imports ---------------------------------------------------------------- #

# System
import os

# Pip
from kcu import kpath

# ---------------------------------------------------------------------------------------------------------------------------------------- #



# ------------------------------------------------------------ Public methods ------------------------------------------------------------ #

def clean_lines():
    for p in kpath.file_paths_from_folder(os.getcwd(), allowed_extensions=__allowed_extesions):
        with open(p, 'r') as f:
            org_text = f.read()
            text = '\n'.join([l.rstrip() for l in org_text.strip().split('\n')]) + '\n'

        if org_text != text:
            print('Cleaning: \'{}\''.format(p))

            with open(p, 'w') as f:
                f.write(text)


# ---------------------------------------------------------- Private properties ---------------------------------------------------------- #

__allowed_extesions = [
    '.py',
    '.js',
    '.ts',
    '.json'
]

