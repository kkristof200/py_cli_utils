# --------------------------------------------------------------- Imports ---------------------------------------------------------------- #

# System
from enum import Enum

# Local
from .utils import comment_line

# ---------------------------------------------------------------------------------------------------------------------------------------- #



# ------------------------------------------------------------- Private vars ------------------------------------------------------------- #

__text='''
# --------------------------------------------------------------- Imports ---------------------------------------------------------------- #

# System
from enum import Enum

# ---------------------------------------------------------------------------------------------------------------------------------------- #



[CLASS_NAME_COMMENT_LINE]

class [CLASS_NAME](Enum):
    Template = 0


# ---------------------------------------------------------------------------------------------------------------------------------------- #
'''.strip()


# ------------------------------------------------------------ Public methods ------------------------------------------------------------ #

def new_enum(class_name: str) -> str:
    return __text.replace('[CLASS_NAME]', class_name).replace('[CLASS_NAME_COMMENT_LINE]', comment_line('enum: {}'.format(class_name)))

